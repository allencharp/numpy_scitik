import numpy as np
a = np.floor(10*np.random.random((2,12)))
print(a)
print(np.hsplit(a,3))
# [[ 3.  5.  8.  9.  6.  9.  2.  6.  2.  3.  6.  8.]
#  [ 2.  6.  8.  4.  3.  8.  4.  9.  3.  5.  9.  2.]]
# [array([[ 3.,  5.,  8.,  9.],
#        [ 2.,  6.,  8.,  4.]]), array([[ 6.,  9.,  2.,  6.],
#        [ 3.,  8.,  4.,  9.]]), array([[ 2.,  3.,  6.,  8.],
#        [ 3.,  5.,  9.,  2.]])]
print(np.vsplit(a,2))
# [array([[ 3.,  5.,  8.,  9.,  6.,  9.,  2.,  6.,  2.,  3.,  6.,  8.]]),
#  array([[ 2.,  6.,  8.,  4.,  3.,  8.,  4.,  9.,  3.,  5.,  9.,  2.]])]